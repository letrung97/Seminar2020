package model;

public class thuc_don {
	private int stt;
	private int ma_nha_hang;
	private String ten_mon;
	private int gia;
	public int getStt() {
		return stt;
	}
	public void setStt(int stt) {
		this.stt = stt;
	}
	public int getMa_nha_hang() {
		return ma_nha_hang;
	}
	public void setMa_nha_hang(int ma_nha_hang) {
		this.ma_nha_hang = ma_nha_hang;
	}
	public String getTen_mon() {
		return ten_mon;
	}
	public void setTen_mon(String ten_mon) {
		this.ten_mon = ten_mon;
	}
	public int getGia() {
		return gia;
	}
	public void setGia(int gia) {
		this.gia = gia;
	}
}
